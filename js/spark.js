/*
 * Objective: Capture live UI state from the simulation iframe.
 * Task: Directly update the spark object's parameter_map with current UI values.
 */
function syncUIToSessionMap(spark) {
    console.group("%c[SYNC DEBUG] 🔍 Distilling UI to Spark Object...", "color: #ffca28; font-weight: bold;");

    const settings = databaseCache?.settings || {};
    const blueprints = settings["arcade-current-types"];
    const blueprint = Array.isArray(blueprints) ? blueprints[spark.index] : null;

    if (!blueprint || !blueprint.parameter_map) {
        console.warn("[SYNC] ❌ No blueprint schema found for index:", spark.index);
        console.groupEnd();
        return;
    }

    const iframeDoc = document.getElementById('content-frame')?.contentWindow?.document;
    if (!iframeDoc) {
        console.error("[SYNC] ❌ Critical: Cannot access iframe document.");
        console.groupEnd();
        return;
    }

    // Ensure the spark has a parameter_map to write to
    if (!spark.parameter_map) spark.parameter_map = {};

    const schemaKeys = Object.keys(blueprint.parameter_map);
    
    schemaKeys.forEach(key => {
        const input = iframeDoc.getElementById(key);
        if (input) {
            let val = input.value;
            const type = input.type;

            // Type-specific extraction
            if (type === 'range' || type === 'number' || type === 'hidden') {
                val = parseFloat(val);
            } else if (type === 'checkbox') {
                val = input.checked;
            }

            // DIRECT MUTATION: Update the spark memory object
            if (spark.parameter_map[key] !== val) {
                console.log(`%c[SYNC] Update: ${key} | ${spark.parameter_map[key]} -> ${val}`, "color: #4caf50; font-weight: bold;");
                spark.parameter_map[key] = val;
            }
        }
    });

    console.log("[SYNC] Distillation Complete. Spark Memory is now current.");
    console.groupEnd();
}

/*
 * Standardizes raw Spark code to fit the responsive Laboratory Viewport.
 * @param {Object} spark - The spark data object from the DB.
 * @returns {string} - The complete HTML string for the iframe.
 */
function wrapCodeInLaboratory(spark) {
    const activeCode = spark.code || (spark.data ? spark.data.code : null) || spark.html || (typeof spark === 'string' ? spark : "");

    return `
        <!DOCTYPE html>
        <html lang="en">
        <head>
            <meta charset="UTF-8">
            <style>
                /* Prevent the 'Double Scrollbar' and Clipping */
                body, html { 
                    margin: 0; 
                    padding: 0; 
                    width: 100%; 
                    height: 100%; 
                    overflow: hidden; /* Critical: Stops the Spark from overflowing the Lab */
                    background: transparent; 
                    display: flex; 
                    flex-direction: column;
                }

                #user-code-wrapper {
                    flex: 1; /* Occupies all available space in the Lab */
                    width: 100%;
                    height: 100%;
                    display: flex;
                    flex-direction: column;
                    justify-content: center;
                    align-items: center;
                    position: relative;
                    box-sizing: border-box;
                }

                /* Standardizes any canvas generated by Gemini to fit within the viewport */
                #user-code-wrapper canvas { 
                    max-width: 100% !important; 
                    max-height: 100% !important;
                    object-fit: contain; 
                    box-sizing: border-box;
                }
            </style>
        </head>
        <body>
            <div id="user-code-wrapper">
                ${activeCode}
            </div>

            <script>
                (function() {
                    const wrapper = document.getElementById('user-code-wrapper');

                    function syncDimensions() {
                        const canvases = document.querySelectorAll('canvas');
                        const bounds = wrapper.getBoundingClientRect();

                        canvases.forEach(canvas => {
                            // If the canvas is the main element, sync its resolution to the Lab Stage
                            // This fixes the "Cut off" issue by ensuring internal resolution = visual size
                            if (canvas.width !== bounds.width || canvas.height !== bounds.height) {
                                // Save current drawing to prevent flicker/loss
                                const tempCtx = canvas.getContext('2d');
                                let tempImage = null;
                                if (tempCtx) {
                                    tempImage = canvas.toDataURL();
                                }

                                canvas.width = bounds.width;
                                canvas.height = bounds.height;

                                if (tempImage) {
                                    const img = new Image();
                                    img.src = tempImage;
                                    img.onload = () => canvas.getContext('2d').drawImage(img, 0, 0, bounds.width, bounds.height);
                                }
                            }
                        });

                        // Notify internal scripts (Three.js/P5) that the Lab has resized
                        window.dispatchEvent(new Event('resize'));
                    }

                    // ResizeObserver is more reliable than window.onresize for iframe/container changes
                    const ro = new ResizeObserver(() => {
                        requestAnimationFrame(syncDimensions);
                    });

                    ro.observe(wrapper);

                    // Initial sync once everything is parsed
                    window.addEventListener('load', syncDimensions);
                })();
            </script>
        </body>
        </html>
    `;
}
/*
 * Objective: Render Spark content and reset HUD visuals.
 * Task: Initialize playable states and reset timing/progress for the new session.
 */
function loadSpark(spark) {
    const container = document.getElementById('spark-content-container');
    const titleEl = document.getElementById('active-spark-name');
    const overlay = document.getElementById('spark-title-overlay');
    const hudStatus = document.getElementById('hud-status');
    const fallbackBtn = document.getElementById('fallback-url-btn');
    
    // 0. HUD CONTROL INITIALIZATION
    window.currentSpark = spark;
    const playPauseBtn = document.getElementById('play-pause-btn');
    const playIcon = document.getElementById('play-icon');
    const curTimeEl = document.getElementById('current-time');
    const totTimeEl = document.getElementById('total-duration');
    const progressFill = document.getElementById('media-progress-bar');

    // CRITICAL FOR TIMING: Reset visuals to zero state immediately
    if (curTimeEl) curTimeEl.textContent = "0:00";
    if (totTimeEl) totTimeEl.textContent = "0:00";
    if (progressFill) progressFill.style.width = "0%";

    // Intelligent Type Detection
    const playableIDs = ['movies', 'videos', 'veo-video', 'arcade-logic', 'games', 'physics-lab', 'optics'];
    const isPlayable = playableIDs.includes(spark.template_type?.toLowerCase()) || 
                       (spark.link && (spark.link.includes('youtube.com') || spark.link.includes('vimeo.com')));

    if (playPauseBtn) {
        playPauseBtn.style.opacity = isPlayable ? '1' : '0.3';
        playPauseBtn.style.pointerEvents = isPlayable ? 'auto' : 'none';
        
        // Default to 'Pause' icon because simulations/videos usually autoplay
        if (playIcon) {
            playIcon.classList.remove('fa-play');
            playIcon.classList.add('fa-pause');
        }
    }

    // 1. IDENTITY & THEME
    if (typeof databaseCache !== 'undefined') {
        applyTheme(globalTheme);
    }
    
    console.log(`%c[YERTAL LAB] Loading Spark: ${spark.name}`, "color: #00f2ff; font-weight: bold;");
        
    container.style.opacity = '0';
    container.innerHTML = '';
    if (hudStatus) hudStatus.textContent = "INITIALIZING...";

    titleEl.textContent = spark.name;
    overlay.style.opacity = "1";
    setTimeout(() => { overlay.style.opacity = "0"; }, 3000);

    // 2. CONTENT INJECTION
    if (spark.link) {
        let finalUrl = spark.link;
        if (finalUrl.includes('youtube.com/watch?v=')) {
            finalUrl = finalUrl.replace('watch?v=', 'embed/') + "?autoplay=1&mute=1&enablejsapi=1";
        }
        
        container.innerHTML = `<iframe id="content-frame" src="${finalUrl}" allow="autoplay; fullscreen"></iframe>`;
        container.style.opacity = '1';
        
    } else {
        const iframe = document.createElement('iframe');
        iframe.id = "content-frame";
        // allow-same-origin is critical for persistence if scripts interact with parent
        iframe.setAttribute('sandbox', 'allow-scripts allow-same-origin allow-forms');
        
        container.appendChild(iframe);

        // Wrap the code
        const standardizedCode = wrapCodeInLaboratory(spark);
        
        /*
         * REVERT TO DOCUMENT.WRITE:
         * Using the fixed template (block comments only), document.write provides 
         * the most stable injection method for dynamic hydration.
         */
        try {
            const doc = iframe.contentWindow.document;
            doc.open();
            doc.write(standardizedCode);
            doc.close();
        } catch (err) {
            console.error("[SYSTEM] Document write failed:", err);
        }

        if (fallbackBtn) fallbackBtn.classList.add('hidden');

        iframe.onload = () => {
            container.style.opacity = '1';
            if (hudStatus) hudStatus.textContent = "SPARK FULLY LOADED";
        };
    }
}

/*
 * Objective: Change viewports to adjacent spark structures.
 * Task: Maintain state continuity across transitions by preserving realm configuration URL parameters.
 */
function navigate(dir) {
    currentIndex = (currentIndex + dir + allSparks.length) % allSparks.length;
    
    const nextSpark = allSparks[currentIndex];
    
    // Highlighted changes to apply: Read and preserve the active user slug
    const currentParams = new URLSearchParams(window.location.search);
    const userParam = currentParams.get('user');
    
    // Highlighted changes to apply: Conditionally append user back into the string
    let newUrl = `${window.location.pathname}?current=${currentId}&spark=${nextSpark.id}`;
    if (userParam) {
        newUrl += `&user=${userParam}`;
    }
    
    window.history.pushState({path: newUrl}, '', newUrl);
    console.log(`navigation detected direction=${dir} currentIndex=${currentIndex}`);
    
    const freshlyAssembled = assembleSpark(nextSpark);
    window.currentSpark = freshlyAssembled;
    loadSpark(freshlyAssembled);
}

