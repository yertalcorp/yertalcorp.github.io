/*
 * Objective: Initialize HUD and navigation interactions.
 * Task: Restrict navigation to mouse-only via side zones and reserve arrow keys for viewport gameplay.
 * Task: Integrate Session Map resets on navigation and exit.
 */
function setupInteractions(currentUid, spark) {
    // 1. Navigation: Mouse-Only via side zones
    const prevZone = document.getElementById('prev-zone');
    const nextZone = document.getElementById('next-zone');

    if (prevZone) {
        prevZone.onclick = (e) => {
            e.stopPropagation();
            e.preventDefault();
            console.log("[NAV] Navigating to Previous Spark...");
            navigate(-1);
        };
    }

    if (nextZone) {
        nextZone.onclick = (e) => {
            e.stopPropagation();
            e.preventDefault();
            console.log("[NAV] Navigating to Next Spark...");
            navigate(1);
        };
    }

    // 2. HUD & Cover Bindings
    const editBtn = document.getElementById('edit-spark-btn');

    if (editBtn) {
        const isOwner = currentUid && spark && spark.owner === currentUid;
        const isSuperUser = currentUid === 'yertal-arcade';

        if (isOwner || isSuperUser) {
            editBtn.style.display = 'flex';
            editBtn.onclick = (e) => {
                e.stopPropagation();
                openSparkEditor(window.currentSpark || spark);
            };
        } else {
            editBtn.style.display = 'none';
        }
    }

    // 3. UI Toggles & Media Controls
    const zenBtn = document.getElementById('zen-btn');
    if (zenBtn) zenBtn.onclick = toggleZen;

    const exitZenBtn = document.getElementById('exit-zen-btn');
    if (exitZenBtn) {
        exitZenBtn.onclick = (e) => {
            e.stopPropagation();
            toggleZen();
        };
    }

    const playPauseBtn = document.getElementById('play-pause-btn');
    if (playPauseBtn) {
        playPauseBtn.onclick = (e) => {
            e.stopPropagation();
            togglePlayPause();
        };
    }

    const contentContainer = document.getElementById('spark-content-container');
    if (contentContainer) {
        contentContainer.onclick = (e) => {
            togglePlayPause();
        };
    }

    // 4. Reload Logic: Now tracking window.currentSpark dynamically
    const reloadBtn = document.getElementById('reload-spark-btn');
    if (reloadBtn) {
        reloadBtn.onclick = () => {
            const activeSpark = window.currentSpark || spark;
            if (activeSpark) {
                console.group("🔄 RELOAD AUDIT");
                // 1. Sync
                syncUIToSessionMap(activeSpark);
                // 2. Assemble
                const freshlyAssembledSpark = assembleSpark(activeSpark);
                // 3. Load
                window.currentSpark = freshlyAssembledSpark;
                loadSpark(freshlyAssembledSpark);
                console.groupEnd();
            }
        };
    }

    // 5. Fallback URL Binding
    const fallbackBtn = document.getElementById('fallback-url-btn');
    if (fallbackBtn) {
        fallbackBtn.onclick = () => {
            const activeSpark = window.currentSpark || spark;
            if (activeSpark && activeSpark.link) {
                window.open(activeSpark.link, '_blank');
            }
        };
    }

    // 6. Exit Logic: Return to Showroom
    const exitBtn = document.getElementById('exit-btn');
    if (exitBtn) {
        exitBtn.addEventListener('click', (e) => {
            e.preventDefault();
            e.stopPropagation();

            const params = new URLSearchParams(window.location.search);
            const userSlug = params.get('user') || 'yertal-arcade';

            const container = document.getElementById('spark-content-container');
            if (container) container.innerHTML = '';

            console.log("[SYSTEM] Exiting to Showroom...");
            window.location.href = `/arcade/index.html?user=${userSlug}`;
        }, true);
    }

    // 8. Keyboard Shortcuts
    window.onkeydown = (e) => {
        const key = e.key.toLowerCase();

        if (key === 'z' || key === 'escape') {
            toggleZen();
        }

        // Ctrl+R Logic: Now tracks live global target execution context
        if (key === 'r' && e.ctrlKey) {
            e.preventDefault();
            const activeSpark = window.currentSpark || spark;
            if (activeSpark) {
                syncUIToSessionMap(activeSpark);
                const freshlyAssembled = assembleSpark(activeSpark);
                window.currentSpark = freshlyAssembled;
                loadSpark(freshlyAssembled);
            }
        }

        if (key === ' ' && e.target.tagName !== 'INPUT' && e.target.tagName !== 'TEXTAREA') {
            e.preventDefault();
            togglePlayPause();
        }
    };

    // 9. Auto-Focus Viewport
    const iframe = document.querySelector('#spark-content-container iframe');
    if (iframe) iframe.focus();
}

/*
 * Objective: Hydrate a blueprint template with the established hierarchy of truth.
 * Task: Ensure session values (Layer 3) take absolute precedence during string replacement.
 */
function assembleSpark(spark) {
    // Highlighted changes to apply:
    if ((spark.code || spark.link) && (spark.index === undefined || spark.index === null)) {
        return spark;
    }

    const settings = databaseCache?.settings || {};
    const blueprints = settings["arcade-current-types"];
    const blueprint = Array.isArray(blueprints) ? blueprints[spark.index] : null;

    if (!blueprint) {
        console.error(`[ASSEMBLER] ❌ FAILED: No blueprint found at index: ${spark.index}`);
        return spark;
    }

    // --- THE HIERARCHY OF TRUTH ---
    // Layer 1: Blueprint Defaults
    // Layer 2: User Spark Node (Saved state)
    // Layer 3: Live Session (Current UI state)
    const finalParameters = { 
        ...(blueprint.parameter_map || {}), 
        ...(spark.parameter_map || {})
    };

    console.groupCollapsed(`[ASSEMBLER] 🛠️ Assembling: ${spark.name}`);
    console.log("Hierarchy Result:", finalParameters);

    let hydratedCode = blueprint.template || "";

    if (hydratedCode) {
        // Iterate through the keys defined in the BLUEPRINT to ensure we don't miss anything
        const schemaKeys = Object.keys(blueprint.parameter_map || finalParameters);
        
        schemaKeys.forEach(key => {
            let value = finalParameters[key];

            // Handle Arrays/Objects
            if (value !== null && typeof value === 'object') {
                value = JSON.stringify(value).replace(/"/g, "'").replace(/'/g, "&apos;");
            }

            const regex = new RegExp(`{{\\s*${key}\\s*(?:\\|\\|\\s*[^}]+)?}}`, 'g');
            
            if (hydratedCode.match(regex)) {
                // This is the moment of truth: check the log for this specific replacement
                console.log(`[BAKING] {{${key}}} -> ${value}`);
                hydratedCode = hydratedCode.replace(regex, () => value);
            }
        });
    }

    console.groupEnd();

    // We return a NEW object to avoid mutating the original reference too early
    return {
        ...spark,
        code: hydratedCode,
        parameter_map: finalParameters 
    };
}


watchAuthState(async (user) => {
    console.log("%c[AUTH] State Changed", "color: #00ff00;");
    if (!user) return;

    userId = user.uid; 
    // load the spark for the user, current, spark defined in the URL
    const params = new URLSearchParams(window.location.search);
    const pageOwnerSlug = params.get('user'); 
    currentId = params.get('current');
    const initialSparkId = params.get('spark');

    // 1. Helper to scan the cache
    const findOwnerInCache = (data) => {
        const users = data?.users || {};
        // Check for explicit slug match OR fallback to the superuser ID
        return Object.keys(users).find(uid => {
            const profile = users[uid].profile || {};
            return profile.slug === pageOwnerSlug || uid === 'yertal-arcade';
        });
    };

    let ownerUid = findOwnerInCache(databaseCache);

    // 2. Fetch if missing
    if (!databaseCache || Object.keys(databaseCache).length === 0 || !ownerUid) {
        console.log("[CACHE] Cache miss for owner:", pageOwnerSlug, ". Fetching...");
        const freshData = await getArcadeData(); 
        ownerUid = findOwnerInCache(freshData);
    }

// 3. RESOLUTION LOGIC REFINEMENT
const targetUid = ownerUid || userId;
let userRecord = databaseCache?.users?.[targetUid];

// Logic change: If targetUid is found but infrastructure is missing, 
// it's likely a partial cache. Force a refresh.
if (!userRecord || !userRecord.infrastructure) {
    console.log("[SYSTEM] Infrastructure missing for:", targetUid, ". Requesting sync...");
    const freshData = await getArcadeData(); // This should hit the actual DB
    userRecord = freshData?.users?.[targetUid];
}

// Ensure the specific current exists
const currents = userRecord?.infrastructure?.currents || {};
const path = currents[currentId];

if (!path) {
    // Fallback: If 'neural-void' isn't found, try to grab the first available current
    const fallbackCurrent = Object.values(currents)[0];
    if (fallbackCurrent) {
        console.warn("[DATA] Redirecting to fallback current infrastructure.");
        path = fallbackCurrent;
    } else {
        console.error("[DATA] No record for current:", currentId);
        return;
    }
}

    // 4. Load the Spark
    const sparksObj = path.sparks || {};
    allSparks = Object.values(sparksObj).sort((a, b) => (a.created || 0) - (b.created || 0));
    
    // Use the ID from the URL or fallback to the first one
    currentIndex = allSparks.findIndex(s => s.id === initialSparkId);

    let activeSpark = null;
    if (currentIndex !== -1) {
        activeSpark = allSparks[currentIndex];
    } else if (allSparks.length > 0) {
        activeSpark = allSparks[0];
    }
    if (activeSpark) {
        // NEW: Assemble the spark before loading
        const preparedSpark = assembleSpark(activeSpark);
        loadSpark(preparedSpark);
    } else {
        document.getElementById('active-spark-name').textContent = "EMPTY CURRENT";
    }
    setupInteractions(user.uid, activeSpark);
});

